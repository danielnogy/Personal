namespace BinaryPlate.Application.Common.Enums;

/// <summary>
/// Represents the text direction enumeration for specifying the direction of text.
/// </summary>
public enum TextDirection
{
    /// <summary>
    /// Represents left-to-right text direction.
    /// </summary>
    Ltr, // Left-to-Right

    /// <summary>
    /// Represents right-to-left text direction.
    /// </summary>
    Rtl  // Right-to-Left
}
